package org.model.graph;

/**
 *
 * @author PC 4060TI
 */
public class Arista {

    private int destino;
    private int peso;

    public Arista(int destino, int peso) {
        this.destino = destino;
        this.peso = peso;
    }

    public int getDestino() {
        return destino;
    }

    public void setDestino(int destino) {
        this.destino = destino;
    }

    public int getPeso() {
        return peso;
    }

    public void setPeso(int peso) {
        this.peso = peso;
    }
    
    
}
